CLASSIFY_PROMPT = """\
Определи тип содержимого на изображении. Ответь ОДНИМ словом из списка:
- table (таблица с данными, строки/столбцы)
- diagram (технологическая схема, блок-схема, граф процесса, чертёж оборудования со связями между элементами)
- chart (график/диаграмма с осями — линейный, столбчатый, круговой и т.п.)
- photo (фотография объекта/образца/установки без структурных данных)
- formula (математическая или химическая формула/уравнение)
- text (скан текстовой страницы без таблиц и схем)
- mixed (на изображении явно несколько типов сразу)
Ответь только одним словом из списка, без пояснений."""

